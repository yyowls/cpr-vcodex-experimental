echo "#define LEXEND_14_FONT_ID ($(font_id ./lexend_14_regular.h ./lexend_14_bold.h ./lexend_14_regular.h ./lexend_14_bold.h))"
echo "#define LEXEND_16_FONT_ID ($(font_id ./lexend_16_regular.h ./lexend_16_bold.h ./lexend_16_regular.h ./lexend_16_bold.h))"
echo "#define LEXEND_18_FONT_ID ($(font_id ./lexend_18_regular.h ./lexend_18_bold.h ./lexend_18_regular.h ./lexend_18_bold.h))"
echo "#define NOTOSANS_10_FONT_ID ($(font_id ./notosans_10_regular.h ./notosans_10_bold.h ./notosans_10_bolditalic.h ./notosans_10_italic.h))"
echo "#define NOTOSANS_12_FONT_ID ($(font_id ./notosans_12_regular.h ./notosans_12_bold.h ./notosans_12_bolditalic.h ./notosans_12_italic.h))"
echo "#define NOTOSANS_14_FONT_ID ($(font_id ./notosans_14_regular.h ./notosans_14_bold.h ./notosans_14_bolditalic.h ./notosans_14_italic.h))"
echo "#define NOTOSANS_16_FONT_ID ($(font_id ./notosans_16_regular.h ./notosans_16_bold.h ./notosans_16_bolditalic.h ./notosans_16_italic.h))"
