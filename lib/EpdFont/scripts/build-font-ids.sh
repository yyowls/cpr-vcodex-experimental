#!/bin/bash

set -e

cd "$(dirname "$0")/../builtinFonts"

if command -v python3 >/dev/null 2>&1; then
  PYTHON_BIN=python3
elif command -v python >/dev/null 2>&1; then
  PYTHON_BIN=python
else
  echo "python3/python not found" >&2
  exit 1
fi

font_id() {
  "$PYTHON_BIN" - "$@" <<'PY'
import hashlib
import sys

total = 0
for path in sys.argv[1:]:
    with open(path, "rb") as f:
        total += int(hashlib.sha256(f.read()).hexdigest(), 16)

value = total % (2 ** 32) - (2 ** 31)
print(value)
PY
}

echo "// The contents of this file are generated by ./lib/EpdFont/scripts/build-font-ids.sh"
echo "#pragma once"
echo ""

echo "#define BOOKERLY_10_FONT_ID ($(font_id ./bookerly_10_regular.h ./bookerly_10_bold.h ./bookerly_10_bolditalic.h ./bookerly_10_italic.h))"
